package edu.emory.cci.bindaas.sts.opensaml2.exception;

public class TokenGeneratorException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public TokenGeneratorException() {
		super();
		
	}

	public TokenGeneratorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public TokenGeneratorException(String arg0) {
		super(arg0);
		
	}

	public TokenGeneratorException(Throwable arg0) {
		super(arg0);
		
	}

}
