package org.rakshak.junit.exception;

public class JunitRunnerException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public JunitRunnerException() {
		super();
		
	}

	public JunitRunnerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public JunitRunnerException(String arg0) {
		super(arg0);
		
	}

	public JunitRunnerException(Throwable arg0) {
		super(arg0);
		
	}

}
